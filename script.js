let currIndex =0
let answered = false
let currquestions;
let score=0
function startQuiz(params){
  let div1 = document.getElementById("div1")
  div1.className=""
  div1.classList.add("off")
    let quizContainer = document.getElementById("quiz-container")
    let questionContainer = document.getElementById("question-container")
    let header = document.getElementById("header")
    let div2 = document.getElementById("div2")
    div2.style.display = "none"
    quizContainer.style.display="none"
    header.style.display = "none"
    questionContainer.style.display = "block"

    currquestions = data.sections[params]["questions"]

    displayQuestion()
}
function displayQuestion(event){
  let questionTag = document.getElementById("question")
  let options = document.getElementById("options")
  options.innerHTML=""
  questionTag.innerHTML = currquestions[currIndex]["question"]
  if(currquestions[currIndex]["questionType"]=="mcq"){
    currquestions[currIndex]["options"].forEach(element => {
      options.innerHTML += `<button class="optionstyle" id="${element}" onclick="selectedOption('${element}')">${element}</button>`
    })
  }
  else{
    options.innerHTML = 
    `<input id="inputQue" type=${currquestions[currIndex]["questionType"]}>
    <button id="submitbtn" class="submit-answer"onclick="selectedOption(getInputValue())">Submit Answer</button>`
  }
}
function getInputValue(){
  let inputvalue = document.getElementById("inputQue").value
  return inputvalue

}

function selectedOption(option){
  let message= document.getElementById("message")
  message.textContent=""
  if(!answered && currquestions[currIndex]["questionType"]=="mcq"){
    let changeSelected = document.getElementById(option)
    let options = document.getElementById("options")
    changeSelected.classList.add("selected")
    answered = true
    const buttons = options.querySelectorAll('button');
    buttons.forEach((button)=>{
      if(button.id !== option){
        button.disabled= true 
      }
    })
    if(option == currquestions[currIndex]["answer"]){
      score+=1
      console.log(score);
      message.textContent = "Correct"
      message.classList.add("success")
    }
    else{
      message.textContent = `Wrong! The correct answer is ${currquestions[currIndex]["answer"]}`
      message.classList.add("error")
    }
  }
  else if(currquestions[currIndex]["questionType"]=="text"){
    let submitbtn = document.getElementById("submitbtn")
    let inputQue = document.getElementById("inputQue")
    inputQue.disabled = true
    submitbtn.disabled = true
    if(option.toLowerCase() == currquestions[currIndex]["answer"].toLowerCase()){
    message.textContent = "Correct"
    message.classList.add("success")
    score+=1
    console.log(score);
    }
    else{
      message.textContent = `Wrong! The correct answer is ${currquestions[currIndex]["answer"]}`
      message.classList.add("error")
    }
  }
  else{
    let submitbtn = document.getElementById("submitbtn")
    let inputQue = document.getElementById("inputQue")
    inputQue.disabled = true
    submitbtn.disabled = true
    if (Number(option) == currquestions[currIndex]["answer"]){
      message.textContent = "Correct"
      message.classList.add("success")
      score+=1
      console.log(score);
      }
      else{
        message.textContent = `Wrong! The correct answer is ${currquestions[currIndex]["answer"]}`
        message.classList.add("error")
      }
  }
}


function gonext() {
  if(currIndex<currquestions.length-1){
    let message= document.getElementById("message")
    message.textContent= ""
    message.className=""
    answered = false
    currIndex +=1
    displayQuestion()
  }
  else{
    let scoreContainer = document.getElementById("scoreContainer")
    let questionContainer = document.getElementById("question-container")
    let scoreTag = document.getElementById("score")
    let img = document.getElementById("img")
    if (score>6) {
      img.src = "https://media.tenor.com/idRqMvuHAKEAAAAM/well-done.gif"
    } else {
      img.src="https://media.tenor.com/uygzHmz_Pt8AAAAi/kitkatandahalf-mood-cyan.gif"
    }
    scoreTag.textContent = `Your Final Score : ${score}/10`

    questionContainer.style.display = "none"
    scoreContainer.style.display = "block"
  }
}

function goHome(){
  let div1 = document.getElementById("div1")
  div1.className=""
  div1.classList.add("on")
  let quizContainer = document.getElementById("quiz-container")
  let scoreContainer = document.getElementById("scoreContainer")
  let questionContainer = document.getElementById("question-container")
  let header = document.getElementById("header")
  let div2 = document.getElementById("div2")
  let message= document.getElementById("message")
  message.textContent= ""
  message.className=""
  div2.style.display = "block"
  quizContainer.style.display="grid"
  header.style.display = "block"
  questionContainer.style.display = "none"
  scoreContainer.style.display="none"
  currIndex =0
  answered = false
  currquestions =null
  score=0

}

